#include <iostream>
using namespace std;
int main() {
    cout<<"The size of integer: "<<sizeof(int)<<endl;
    cout<<"The size of float: "<<sizeof(float)<<endl;
    cout<<"The size of double: "<<sizeof(double)<<endl;
    cout<<"The size of character: "<<sizeof(char)<<endl;
    cout<<"The size of short: "<<sizeof(short)<<endl;
    cout<<"The size of boolean: "<<sizeof(bool)<<endl;
    return 0;
}